public class WhileCondition //create a class
{
   public static void main(String[] args) {
        int i=1;  //define a variable
		while(i<=20) //entry of while condition and check the condition
		{
			System.out.println(i);  //if the condition is satisfy ,print integer
			i++; //increment for the integer value until the condition has been satisfied
		}

	}

}


